import * as React from "react"

import { cn } from "@/lib/utils"

const cardVariants = (props: { variant?: string } = {}) => {
  const baseClasses = "rounded-xl border bg-card text-card-foreground shadow-sm";
  
  const variantClasses = {
    default: "rounded-xl border bg-card text-card-foreground shadow-sm",
    hover: "rounded-xl border bg-card text-card-foreground shadow-sm",
  };
  
  const variant = props.variant || "default";
  return `${baseClasses} ${variantClasses[variant as keyof typeof variantClasses] || variantClasses.default}`.trim();
};

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border bg-card text-card-foreground shadow-sm hover:shadow-md transition-shadow duration-200",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

